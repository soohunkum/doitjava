package chapter04;

public class ForEx3 {

	public static void main(String[] args) {
		int i, j;
		
		for(i=0; i<3; i++) {
			for(j=0; j<5; j++) {
				System.out.print("*");
			}
				System.out.println();
		}
		
		
		for(i=0; i<3; i++) {
			System.out.println("Hello!");
		}
	}
}
